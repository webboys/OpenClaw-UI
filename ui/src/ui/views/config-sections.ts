function humanizeSectionKey(key: string): string {
  const spaced = key.replace(/([a-z0-9])([A-Z])/g, "$1 $2").replace(/[_-]+/g, " ");
  return spaced
    .split(" ")
    .filter(Boolean)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(" ");
}

export function renderConfigSectionIcon(key: string) {
  const normalized = key.trim();
  const direct = sectionIcons[normalized as keyof typeof sectionIcons];
  if (direct) {
    return direct;
  }
  const lower = normalized.toLowerCase();
  return sectionIcons[lower as keyof typeof sectionIcons] ?? sectionIcons.default;
}

export function resolveConfigSectionMeta(key: string, schema?: JsonSchema): ConfigSectionMeta {
  const normalized = key.trim();
  const meta = CONFIG_SECTION_META[normalized] ?? CONFIG_SECTION_META[normalized.toLowerCase()];
  if (meta) {
    return meta;
  }
  return {
    label: localizeConfigText(schema?.title ?? humanizeSectionKey(key), key),
    description: localizeConfigHelp(schema?.description, key),
  };
}
